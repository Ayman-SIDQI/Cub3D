#include "libft.h"

short	ft_isdigit(char *c)
{
	int	i;

	i = 0;
	while (/*c[i] && */c[i] >= '0' && c[i] <= '9')
	{
		i++;
	}
	if (c[i] != '\0')
		return (1);
	return (0);
}
